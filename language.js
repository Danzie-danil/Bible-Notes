window.t = function(key) {
  const lang = (typeof state !== 'undefined' && state.appLang) ? state.appLang : 'en';
  return (translations[lang] && translations[lang][key]) ? translations[lang][key] : key;
};

function applyTranslations(lang) {
  const defaultLang = 'en';
  const t = translations[lang] || translations[defaultLang];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      el.textContent = t[key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) {
      el.setAttribute('placeholder', t[key]);
    }
  });

  // Handle dynamic greetings if present
  if (typeof state !== 'undefined') {
    const greetingEl = document.getElementById('home-greeting');
    if (greetingEl) {
      const hr = new Date().getHours();
      let msg = t.good_morning;
      if (hr >= 12 && hr < 17) msg = t.good_afternoon;
      else if (hr >= 17) msg = t.good_evening;
      greetingEl.textContent = `${msg}, ${state.userName ? state.userName.split(' ')[0] : ''} ✨`;
    }
  }
}
